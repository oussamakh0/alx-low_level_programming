#include "main.h"
/**
 *print_sign - print the sign og the number
 *@n: number to be checked
 *Return: either + or - or 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}

